package com.tech.java;

public class DataTypeExample {
	public static void main(String[] args) {
		byte age=45;
		short billno=453;
		int bookno=324333;
		long mobileno=23432433L;
		float interestrate=4.5F;
		double calculatedinterest=23323.333;
		System.out.println("Age is "+age);
		System.out.println("Bill no is "+billno);
		System.out.println("Book no is"+bookno);
		System.out.println("Mobile no is "+mobileno);
		System.out.println("interest rate is "+interestrate);
		System.out.println("calculated interest is "+calculatedinterest);

}
}